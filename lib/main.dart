// WorkManager için callback fonksiyonu
@pragma('vm:entry-point')
void callbackDispatcher() {
  Workmanager().executeTask((task, inputData) async {
    try {
      // NotificationService'deki callback fonksiyonunu çağır
      await NotificationService.checkForNewEarthquakes();
      return true;
    } catch (e) {
      developer.log('Arka plan görevi hatası: $e', name: 'BackgroundTask');
      return false;
    }
  });
}

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  
  // WorkManager'ı başlat
  await Workmanager().initialize(
    callbackDispatcher,
    isInDebugMode: false,
  );
  
  runApp(const MyApp());
}

